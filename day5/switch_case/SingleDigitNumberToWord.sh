		;;
	2)
		echo "$n is Two"
		;;
	3)
		echo "$n is Three"
		;;
	4)
		echo "$n is Four"
		;;
	5)
		echo "$n is Five"
		;;
	6)
		echo "$n is Six"
		;;
	7)
		echo "$n is Seven"
		;;
	8)
		echo "$n is Eight"
		;;
	9)
		echo "$n is Nine"
		;;
	*)
		echo "$n is Zero"
		;;
esac

